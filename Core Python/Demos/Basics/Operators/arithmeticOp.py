### Arithmetic Operators

x = 20
y = 10
a = 'ABC'
b = 'XYZ'


# 1. +
# Perform addition / concatenate strings

res = x + y
print(res)

# Concatenation → Join two strings
res = a + b
print(res)


# 2. -
# Perform subtraction
res = x - y
print(res)

# 3. *
# Perform multiplication
res = x * y
print(res)

# 4. /
# Perform division and return float result
res = x / y
print(res)

# 5. //
# Perform floor division and return quotient
res = 5.0 // 2.0
print(res)

# 6. %
# Return remainder after division
res = 5 % 2
print(res)

# 7. **
# Perform power operation
res = 5 ** 2
print(res)
