### Membership Operator

li = [10,20,30,40,50]

#1 in
# Check value exists in sequence
print(20 in li)

# Check substring exists in string
print('first' in 'Firstbit Solutions')


#2 not in
# Check value does not exist in sequence
print(70 not in li)

# Check substring does not exist in string
print('first' not in 'Firstbit Solutions')