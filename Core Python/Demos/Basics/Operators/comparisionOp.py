### Comparision Operator / Relational Operator

x=10
y=20

#1 == (Exact equal to )
# Compare two values and return True if equal
print(x==10)
print(x==20)


#2 !=
# Compare two values and return True if not equal
print(x!='10')


#3 >
# Check left value is greater than right value
print(x>8)


#4 >=
# Check left value is greater than or equal to right value
print(x>=101)


#5 <
# Check left value is smaller than right value
print(x<20)


#6 <=
# Check left value is smaller than or equal to right value
print(x<=20)
